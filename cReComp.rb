	i = 0
	if reg2fifo_32_max_r > 0
		while i < reg2fifo_32_max_r
			bitmax = bitmin.to_i + bit_witdh_32_r[i].to_i - 1;
			fo.puts("\t\t#{reg2fifo_stack_32_r[i]} <= din_32[#{bitmax}:#{bitmin}];\n")
			i = i + 1
			bitmin = bitmax.to_i + 1
		end
	end
	while true
		l = fi.gets.chomp
		if l == "/*user defined init*/"
			fo.puts(l)
			break
		end
		fo.puts(l)
	end
