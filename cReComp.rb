		reg2fifo = l.split(",")
		cur = cur +  reg2fifo[0].to_i

		if cur > 32 then
			print "error! Uupper limit of 32bit FIFO was exceeded\n\n"
		elsif 
			bit_witdh_32_s[ix] = reg2fifo[0]
			reg2fifo_stack_32_s[ix] = reg2fifo[1]
