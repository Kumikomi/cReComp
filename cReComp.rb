	bitmin = 0
	if reg2fifo_32_max_s > 0
		while i < reg2fifo_32_max_s
			bitmax = bitmin.to_i + bit_witdh_32_s[i].to_i - 1;
			fo.puts("\t\tdout_32[#{bitmax}:#{bitmin}] <= #{reg2fifo_stack_32_s[i]};\n")
			i = i + 1
			bitmin = bitmax.to_i + 1
		end
	end
	while l = fi.gets
		fo.puts(l)
	end

end

