			elsif reg2fifo[2] == "w"
				fo.puts "wire [#{reg2fifo[0].to_i-1}:0] #{reg2fifo[1]};\n"
			else
				fo.puts "reg [#{reg2fifo[0].to_i-1}:0] #{reg2fifo[1]};\n"
